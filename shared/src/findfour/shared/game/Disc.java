package findfour.shared.game;

/**
 * Represents the possible states a disc can have.
 * @author ciske
 * 
 */
public enum Disc {
    /**
     * A disk owned by the red player.
     */
    Red,
    /**
     * A disk owned by the yellow player.
     */
    Yellow,
    /**
     * Represents the absence of a disk.
     */
    None;

}
